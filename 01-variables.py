# Tipo: int (entero)
edad = 25
print(type(edad))  # <class 'int'>

# Tipo: float (n√∫mero decimal)
altura = 1.75
print(type(altura))  # <class 'float'>

# Tipo: str (cadena de texto)
nombre = "Ana"
print(type(nombre))  # <class 'str'>

# Tipo: bool (booleano)
es_mayor = True
print(type(es_mayor))  # <class 'bool'>

# Tipo: list (lista)
numeros = [1, 2, 3, 4]
print(type(numeros))  # <class 'list'>

# Tipo: tuple (tupla)
coordenadas = (10.5, 20.3)
print(type(coordenadas))  # <class 'tuple'>

# Tipo: dict (diccionario)
persona = { "nombre": "Ana", 
            "edad": 25
            }


print(type(persona))  # <class 'dict'>

# Tipo: set (conjunto)
colores = {"rojo", "azul", "verde"}
print(type(colores))  # <class 'set'>

# Tipo: NoneType (valor nulo)
vacio = None
print(type(vacio))  # <class 'NoneType'>
