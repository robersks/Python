
# METODOS DE UN STRING

texto = "Hola Mundo"

print(texto.upper()) #Poner en mayusculas
print(texto.lower()) #Poner en minuscula
print(texto.find("Mundo"))  #busca el indice en el que se encuentra "Mundo" comenzando desde indice zero
print(texto.replace("Mundo","Fabian")) # busca y reemplaza texto en un string
print("Hola" in texto) # Buscar si "Hola" se encuentra dentro del string